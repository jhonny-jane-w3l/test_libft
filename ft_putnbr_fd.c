#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	digit;

	if (n == -2147483648)
		write(fd, "-2147483648", 12);
	if (n < 0 && n != -2147483648)
	{
		n = -n;
		write(fd, "-", 1);
	}
	if (n >= 9)
	{
		ft_putnbr_fd(n / 10, fd);
	}
	digit = n % 10 + 48;
	write (fd, &digit, 1);
}
